export function findSwissLocation(name: string): SwissLocation | null {
  const normalized = name.toLowerCase().trim();
  return SWISS_LOCATIONS.find(
    loc => loc.name.toLowerCase() === normalized
  ) || null;
}

export function searchSwissLocations(query: string, limit = 20): SwissLocation[] {
  const q = query.toLowerCase().trim();
  if (!q) return SWISS_LOCATIONS.slice(0, limit);
  return SWISS_LOCATIONS
    .filter(loc => loc.name.toLowerCase().includes(q))
    .slice(0, limit);
}